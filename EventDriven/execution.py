logger = setup_logger("EventDriven.execution", stream_log_level="WARNING")
exec_cache = {"order": {}, "fill": {}, "exercise": {}}
fill_tracking = {}  # Tracks spread & commission per fill
# execution.py


class ExecutionHandler(object):
    """
    The ExecutionHandler abstract class handles the interaction
    between a set of order objects generated by a Portfolio and
    the ultimate set of Fill objects that actually occur in the
    market.

    The handlers can be used to subclass simulated brokerages
    or live brokerages, with identical interfaces. This allows
    strategies to be backtested in a very similar manner to the
    live trading engine.
    """

    __metaclass__ = ABCMeta

    @abstractmethod
    def execute_order_naively(self, event):
        """
        Takes an Order event and executes it, producing
        a Fill event that gets placed onto the Events queue.

        Parameters:
        event - Contains an Event object with order information.
        """
        raise NotImplementedError("Should implement execute_order()")


class SimulatedExecutionHandler(ExecutionHandler):
    """
    The simulated execution handler simply converts all order
    objects into their equivalent fill objects automatically
    without latency, slippage or fill-ratio issues.

    This allows a straightforward "first go" test of any st
    rategy,
    before implementation with a more sophisticated execution
    handler.
    """

    def __init__(
        self,
        events,
        max_slippage_pct: float = 0.002,
        commission_rate: float = 0.0065,
        config: Optional[ExecutionHandlerConfig] = None,
        liquidity_policy: Optional[LiquidityPolicy] = None,
    ):
        """
        Initialises the handler, setting the event queues
        up internally.

        Parameters:
        events - The Queue of Event objects.
        max_slippage_pct - The slippage range for the market default is 0.002
        commission_rate - The commission rate for the market default is 35 cents per contract.
            Option price is in contract units, so commission is also in contract units. For example, if commission_rate is 0.0035, then scaled commission for 1 contract is 0.0035 * 100 = $0.35,

        """
        self.events = events
        self.max_slippage_pct = max_slippage_pct
        self.min_slippage_pct = max_slippage_pct / 2  # Minimum slippage is half of max slippage
        self.commission_rate = commission_rate
        self.config: ExecutionHandlerConfig = config or ExecutionHandlerConfig()
        self.liquidity_policy = liquidity_policy or LiquidityPolicy()
        self.liquidity_drops: list[dict] = []
        self.drop_counts_by_signal_trade: dict[tuple[str, str], int] = {}
        self.fill_tracking: list[dict] = []  # Track spread & commission per fill

    def _spread_pct(self, order_event: OrderEvent) -> Optional[float]:
        """Returns spread percentage as spread/close, or None if unavailable."""
        position = order_event.position or {}
        spread = position.get("spread")
        close = position.get("close")
        if spread is None or close in [None, 0]:
            return None
        try:
            return abs(float(spread) / float(close) * self.config.pct_alpha)
        except Exception:
            return None

    def _track_fill_metrics(
        self, order_event: OrderEvent, spread: Optional[float], commission: float, slippage: float, slippage_model: str
    ):
        """Track spread & commission metrics for each fill."""
        position = order_event.position or {}
        close = position.get("close")
        spread_pct = None
        if spread is not None and close not in [None, 0]:
            spread_pct = abs(float(spread) / float(close))

        tracking_entry = {
            "signal_id": order_event.signal_id,
            "trade_id": position.get("trade_id", ""),
            "datetime": order_event.datetime,
            "direction": order_event.direction,
            "symbol": order_event.symbol,
            "quantity": order_event.quantity,
            "close": close,
            "spread": spread,
            "spread_pct": spread_pct,
            "commission": commission,
            "slippage": slippage,
            "slippage_model": slippage_model,
        }
        self.fill_tracking.append(tracking_entry)
        key = f"{order_event.signal_id}::{order_event.datetime.strftime('%Y-%m-%d')}::{order_event.direction}"
        fill_tracking[key] = tracking_entry
        logger.debug(
            f"Fill tracking: signal={order_event.signal_id}, spread={spread}, spread_pct={spread_pct}, "
            f"commission={commission}, slippage={slippage}, model={slippage_model}"
        )

    def _track_drop(self, order_event: OrderEvent, spread_pct: float):
        """Track silent drops by signal/trade pair and keep a detailed event log."""
        trade_id = (order_event.position or {}).get("trade_id", "")
        key = (str(order_event.signal_id), str(trade_id))
        count = self.drop_counts_by_signal_trade.get(key, 0) + 1
        self.drop_counts_by_signal_trade[key] = count
        self.liquidity_drops.append(
            {
                "signal_id": order_event.signal_id,
                "trade_id": trade_id,
                "datetime": order_event.datetime,
                "spread_pct": spread_pct,
                "threshold": self.liquidity_policy.max_spread_pct,
                "drop_count_for_pair": count,
            }
        )

    def _maybe_drop_for_liquidity_level_2(self, order_event: OrderEvent) -> bool:
        """Level 2 liquidity gate: silently drop and reschedule order when spread_pct is too wide."""
        if not self.liquidity_policy.enabled(2):
            return False

        spread_pct = self._spread_pct(order_event)
        if not self.liquidity_policy.should_drop_for_spread(spread_pct):
            return False

        self._track_drop(order_event, spread_pct)

        # Silent drop with retry: move the same order event to the next business day.
        next_trading_day = pd.to_datetime(order_event.datetime) + pd.offsets.BusinessDay(1)
        next_order_event = deepcopy(order_event)
        next_order_event.datetime = next_trading_day
        self.events.schedule_event(next_trading_day, next_order_event)
        logger.info(
            f"Liquidity level-2 drop for signal {order_event.signal_id}, trade {(order_event.position or {}).get('trade_id', '')}. "
            f"spread_pct={spread_pct:.4f} > {self.liquidity_policy.max_spread_pct:.4f}. Rescheduled to {next_trading_day}."
        )
        return True

    def execute_order_naively(self, order_event: OrderEvent):
        """
        Simply converts Order objects into Fill objects naively,
        i.e. without any latency, slippage or fill ratio problems.

        Parameters:
        event - Contains an Event object with order information.
        """

        ##TODO: Need to add market_value here
        if order_event.type == "ORDER":
            fill_event = FillEvent(
                order_event.datetime,
                order_event.symbol,
                "ARCA",
                order_event.quantity,
                order_event.direction,
                fill_cost=0,
                commission=None,
                option=order_event.option,
                parent_event=order_event,
            )
            self.events.put(fill_event)

    def calculate_slippage_value_randomized(self, order_event: OrderEvent) -> float:
        """
        Calculate slippage value based on a random percentage within the specified slippage range.
        The slippage percentage is positive for BUY orders (increasing the price) and negative for SELL orders (decreasing the price).

        Creates a tracking entry for spread & commission metrics.
        """
        if order_event.direction == "BUY":
            slippage_pct = np.random.uniform(
                self.min_slippage_pct, self.max_slippage_pct
            )  ## Ensure that slippage is always positive, and never 0 or more than max_slippage_pct
        elif order_event.direction == "SELL":
            slippage_pct = np.random.uniform(
                -self.max_slippage_pct, -self.min_slippage_pct
            )  ## Ensure that slippage is always negative, and never 0 or less than -max_slippage_pct
        else:
            raise ValueError(f"Invalid order direction: {order_event.direction}. Must be 'BUY' or 'SELL'.")

        slippage_value = order_event.position["close"] * slippage_pct * order_event.quantity

        # Track spread & commission for this fill
        spread = order_event.position.get("spread")
        commission = (
            self.commission_rate
            * order_event.quantity
            * (len(order_event.position.get("trade_id", "&L:").split("&")) - 1)
        )
        self._track_fill_metrics(order_event, spread, commission, slippage_value, "randomized")

        return slippage_value

    def calculate_slippage_value_fixed(self, order_event: OrderEvent) -> float:
        """
        Calculate slippage value based on a fixed percentage defined by max_slippage_pct.
        The slippage percentage is positive for BUY orders (increasing the price) and negative for SELL orders (decreasing the price).

        Creates a tracking entry for spread & commission metrics.
        """
        if order_event.direction == "BUY":
            slippage_pct = self.max_slippage_pct
        elif order_event.direction == "SELL":
            slippage_pct = -self.max_slippage_pct
        else:
            raise ValueError(f"Invalid order direction: {order_event.direction}. Must be 'BUY' or 'SELL'.")

        slippage_value = order_event.position["close"] * slippage_pct * order_event.quantity

        # Track spread & commission for this fill
        spread = order_event.position.get("spread")
        commission = (
            self.commission_rate
            * order_event.quantity
            * (len(order_event.position.get("trade_id", "&L:").split("&")) - 1)
        )
        self._track_fill_metrics(order_event, spread, commission, slippage_value, "fixed")

        return slippage_value

    def calculate_slippage_pct_of_spread(self, order_event: OrderEvent) -> float:
        """
        Calculate slippage value as a percentage of the bid-ask spread.
        The slippage percentage is positive for BUY orders (increasing the price) and negative for SELL orders (decreasing the price).

        Creates a tracking entry for spread & commission metrics.
        """
        spread = order_event.position.get("spread", None)
        if spread is None:
            raise ValueError(
                "Spread information is required in the order_event position data to calculate slippage as a percentage of the spread."
            )
        spread_slippage = spread * self.config.pct_alpha
        if order_event.direction == "BUY":
            slippage = spread_slippage * order_event.quantity
        elif order_event.direction == "SELL":
            slippage = -spread_slippage * order_event.quantity
        else:
            raise ValueError(f"Invalid order direction: {order_event.direction}. Must be 'BUY' or 'SELL'.")
        close = order_event.position["close"]
        pct_spread_slippage = (spread_slippage / close) if close != 0 else 0
        logger.info(
            f"Spread: {spread}, Spread Slippage: {spread_slippage}, Total Slippage: {slippage}, Pct Spread Slippage: {pct_spread_slippage}, Signal ID: {order_event.signal_id}, Direction: {order_event.direction}, Date: {order_event.datetime}"
        )

        # Track spread & commission for this fill
        commission = (
            self.commission_rate
            * order_event.quantity
            * (len(order_event.position.get("trade_id", "&L:").split("&")) - 1)
        )
        self._track_fill_metrics(order_event, spread, commission, slippage, "spread_pct")

        return slippage

    def calculate_slippage_value_none(self, order_event: OrderEvent) -> float:
        """
        Calculate zero slippage value.
        Used when slippage_model is 'none' for perfect execution at market price.

        Creates a tracking entry for spread & commission metrics.

        Args:
            order_event: The order event (unused, but kept for interface consistency).

        Returns:
            float: Always returns 0.0 (no slippage).
        """
        # Track spread & commission for this fill (with zero slippage)
        spread = order_event.position.get("spread")
        commission = (
            self.commission_rate
            * order_event.quantity
            * (len(order_event.position.get("trade_id", "&L:").split("&")) - 1)
        )
        self._track_fill_metrics(order_event, spread, commission, 0.0, "none")

        return 0.0

    def calculate_slippage_value(self, order_event: OrderEvent) -> float:
        """
        Calculate slippage value based on the specified slippage model in the config.

        Args:
            order_event: The order event containing position and direction information.

        Returns:
            float: The calculated slippage value.

        Raises:
            ValueError: If an invalid slippage model is configured.
        """
        if self.config.slippage_model == "randomized":
            return self.calculate_slippage_value_randomized(order_event)
        elif self.config.slippage_model == "fixed":
            return self.calculate_slippage_value_fixed(order_event)
        elif self.config.slippage_model == "spread_pct":
            return self.calculate_slippage_pct_of_spread(order_event)
        elif self.config.slippage_model == "none":
            return self.calculate_slippage_value_none(order_event)
        else:
            raise ValueError(
                f"Invalid slippage model: {self.config.slippage_model}. Must be 'randomized', 'fixed', 'spread_pct', or 'none'."
            )

    def execute_order(self, order_event: OrderEvent):
        """
        This method will execute an order event, calculate the fill cost including slippage and commission, and put a fill event on the queue.
        """
        assert order_event.type == "ORDER", f"Event type must be 'ORDER' received {order_event.type}"
        assert order_event.direction == "BUY" or order_event.direction == "SELL", (
            f"Event direction must be 'BUY' or 'SELL' received {order_event.direction}"
        )

        if self._maybe_drop_for_liquidity_level_2(order_event):
            return

        exec_cache["order"][f"{order_event.signal_id}_{order_event.datetime.strftime('%Y-%m-%d')}"] = deepcopy(
            order_event
        )
        # Generate slippage as a percentage
        ## Slippage improvement
        if order_event.direction == "BUY":
            ## We want to increase the price for buys by slippage
            slippage_pct = np.random.uniform(
                self.min_slippage_pct, self.max_slippage_pct
            )  ## Ensure that slippage is always positive, and never 0 or more than max_slippage_pct
        elif order_event.direction == "SELL":
            ## We want to decrease the price for sells by slippage
            slippage_pct = np.random.uniform(
                -self.max_slippage_pct, -self.min_slippage_pct
            )  ## Ensure that slippage is always negative, and never 0 or less than -max_slippage_pct

        slippage_pct_value = self.calculate_slippage_value(order_event)
        slippage_per_contract = slippage_pct_value / order_event.quantity if order_event.quantity != 0 else 0
        slippage_pct = (
            slippage_per_contract / order_event.position["close"] if order_event.position["close"] != 0 else 0
        )

        # slippage may increase or decrease intended price
        price = order_event.position["close"] * (1 + slippage_pct)

        # Ensuring cash doesn't go below zero
        raw_quantity = order_event.quantity

        try:
            if raw_quantity is not None:
                # Recompute quantity downward if cost exceeds available cash
                unit_cost = price + self.commission_rate
                logger.info(
                    f"Unit cost: {unit_cost}, Cash available: {order_event.cash}, Direction: {order_event.direction}, Signal ID: {order_event.signal_id}"
                )
                if order_event.direction == "BUY":
                    max_affordable_quantity = math.floor(order_event.cash / unit_cost)

                    # Ensure we never exceed max affordable quantity
                    quantity = min(raw_quantity, max_affordable_quantity)
                    total_cost = quantity * price + self.commission_rate

                    ## Clamp quantity to ensure we don't exceed available cash
                    while total_cost > order_event.cash:
                        logger.info(f"Total cost {total_cost} exceeds available cash {order_event.cash}. Reducing quantity.")
                        quantity -= 1
                        total_cost = quantity * price + self.commission_rate
                    logger.info(
                        f"Max affordable quantity: {max_affordable_quantity}, Raw quantity: {raw_quantity}, Final quantity: {quantity}, Signal ID: {order_event.signal_id}, Total Cost: {quantity * price + self.commission_rate}, Cash: {order_event.cash}"
                    )

                elif order_event.direction == "SELL":
                    # For SELL, we can only sell what we have in the position
                    quantity = raw_quantity
            else:
                # Fall back to normal logic
                quantity = math.floor(order_event.cash / (price + self.commission_rate))
        except:
            pass

        commission = (
            self.commission_rate * quantity * (len(order_event.position.get("trade_id", "&L:").split("&")) - 1)
        )  # commission is per trade(leg) there should always be a long in a position, naked or spread

        market_value = (
            order_event.position["close"] * quantity
        )  # market value is based on the position's close price, not the slippage adjusted price
        # This is to ensure that the market value is not affected by slippage, as slippage is a cost incurred after the market value is determined.

        # Adjust price based on order direction
        if order_event.direction == "BUY":
            fill_cost = (price * quantity) + commission  ## Total Cost for BUY includes commission and slippage
        elif order_event.direction == "SELL":
            fill_cost = (price * quantity) - commission

        ##NOTE:
        # - Market value is based on the position's close price, not the slippage adjusted price. This serves as entry market value
        # - Fill cost is based on the slippage adjusted price and commission. This serves as entry cost
        # - Market value != fill cost, as market value is based on the position's close price, not the slippage adjusted price.

        slippage_diff = (price - order_event.position["close"]) * quantity
        fill_event = FillEvent(
            order_event.datetime,
            order_event.symbol,
            "ARCA",
            quantity,
            order_event.direction,
            fill_cost=fill_cost,
            market_value=market_value,
            commission=commission,
            position=order_event.position,
            slippage=slippage_diff,
            signal_id=order_event.signal_id,
            parent_event=order_event,
        )
        exec_cache["fill"][
            f"{order_event.signal_id}_{order_event.datetime.strftime('%Y-%m-%d')}_{order_event.direction}"
        ] = deepcopy(fill_event)
        if quantity > 0:
            self.events.put(fill_event)
        else:
            logger.warning(
                f"Calculated quantity is zero or negative for signal {order_event.signal_id} after slippage and cash constraints. Order will not be executed. Final quantity: {quantity}, Signal ID: {order_event.signal_id}"
            )

    def execute_exercise(self, exercise_event: ExerciseEvent):
        """
        This method will execute an exercise event, calculate the pnl of the exercise and put a fill event on the queue
        """
        assert exercise_event.type == "EXERCISE", f"Event type must be 'EXERCISE' received {exercise_event.type}"
        long_pnl = 0.0
        short_pnl = 0.0
        if exercise_event.long_premiums and "long" in exercise_event.position:
            assert all(option in exercise_event.position["long"] for option in exercise_event.long_premiums.keys()), (
                f"option_id in premiums must be present in position. long_premium: {exercise_event.long_premiums.keys()}  long position: {exercise_event.position['long']}"
            )
            assert len(exercise_event.long_premiums) == len(exercise_event.position["long"]), (
                f"number of options in long_premiums must be equal to number of options in long position. long_premium: {len(exercise_event.long_premiums)}  long position: {len(exercise_event.position['long'])}"
            )
            for option_id, premium in exercise_event.long_premiums.items():
                option_meta = parse_option_tick(option_id)
                long_pnl += self.__calculate_premium_pnl(option_meta, exercise_event.spot, premium)

        if exercise_event.short_premiums and "short" in exercise_event.position:
            assert all(option in exercise_event.position["short"] for option in exercise_event.short_premiums.keys()), (
                f"option_id in premiums must be present in position. short_premium: {exercise_event.short_premiums.keys()}  short position: {exercise_event.position['short']}"
            )
            assert len(exercise_event.short_premiums) == len(exercise_event.position["short"]), (
                f"number of options in short_premiums must be equal to number of options in short position. short_premium: {len(exercise_event.short_premiums)}  short position: {len(exercise_event.position['short'])}"
            )
            for option_id, premium in exercise_event.short_premiums.items():
                option_meta = parse_option_tick(option_id)
                short_pnl += self.__calculate_premium_pnl(option_meta, exercise_event.spot, premium)

        total_pnl = long_pnl + short_pnl
        market_value = exercise_event.spot * exercise_event.quantity

        fill_event = FillEvent(
            exercise_event.datetime,
            exercise_event.symbol,
            "ARCA",
            exercise_event.quantity,
            "EXERCISE",
            fill_cost=total_pnl,
            position=exercise_event.position,
            market_value=market_value,
            signal_id=exercise_event.signal_id,
            parent_event=exercise_event,
        )
        self.events.put(fill_event)

    def __calculate_premium_pnl(self, option_meta, spot, premium):
        if option_meta["option_type"] == "C":
            return max(0, spot - option_meta["strike"]) - premium
        elif option_meta["option_type"] == "P":
            return max(0, option_meta["strike"] - spot) - premium
        else:
            raise ValueError(f"Invalid option type: {option_meta['option_type']}")

    def get_fill_tracking_df(self) -> pd.DataFrame:
        """
        Returns fill tracking data as a DataFrame.

        Provides detailed spread, commission, and slippage metrics for each fill
        executed during the backtest. Useful for analyzing transaction costs and
        execution quality.

        Returns:
            pd.DataFrame: DataFrame with columns:
                - signal_id: Signal identifier
                - trade_id: Trade identifier
                - datetime: Execution datetime
                - direction: BUY or SELL
                - symbol: Ticker symbol
                - quantity: Number of contracts
                - close: Close price used for execution
                - spread: Bid-ask spread from position data
                - spread_pct: Spread as percentage of close price
                - commission: Total commission charged
                - slippage: Total slippage cost
                - slippage_model: Slippage model used (randomized, fixed, spread_pct, none)

        Examples:
            >>> handler = SimulatedExecutionHandler(events)
            >>> # ... run backtest ...
            >>> df = handler.get_fill_tracking_df()
            >>> avg_commission = df['commission'].mean()
            >>> avg_spread_pct = df['spread_pct'].mean()
        """
        if not self.fill_tracking:
            return pd.DataFrame()
        return pd.DataFrame(self.fill_tracking)
